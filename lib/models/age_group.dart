class AgeGroup {
  static bool is17to21(int age) {
    bool is17to21 = age >= 17 && age <= 21;
    return is17to21;
  }

  static bool is22to26(int age) {
    bool is22to26 = age >= 22 && age <= 26;
    return is22to26;
  }
  static bool is27to31(int age) {
    bool is27to31 = age >= 27 && age <= 31;
    return is27to31;
  }
}
