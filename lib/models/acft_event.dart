class AcftEvent {
  final String eventName;
  final String subtitle;
  final String description;
  final String instructions;
  final String safetyTips;
  final String fitnessComponents;
  final String standardEquipment;
  final String videoPath;
  final String eventImagePath;


  AcftEvent({
    required this.eventName,
    required this.subtitle,
    required this.description,
    required this.instructions,
    required this.safetyTips,
    required this.fitnessComponents,
    required this.standardEquipment,
    required this.videoPath,
    required this.eventImagePath
  });

}